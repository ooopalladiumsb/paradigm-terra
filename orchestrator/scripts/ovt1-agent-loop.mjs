#!/usr/bin/env node
// OVT-1 H1.4 — autonomous agent-loop falsification test.
//
// Hypothesis: an agent completes the FULL cycle (construct → operator_sign → owner_sign →
// MCP-execute → submit → FINALIZED) with NO manual intervention. One run exercises nonce lifecycle,
// expiration handling, signature plumbing, submit/await, finalized detection, and replay safety.
//
//   node --import tsx scripts/ovt1-agent-loop.mjs      # from orchestrator/

import path from "node:path";
import { fileURLToPath } from "node:url";
import { OvtAgent, LocalTestOwnerSigner } from "../src/agent/ovt-agent.js";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(HERE, "../..");
const serverPath = path.join(ROOT, "orchestrator/src/mcp/test-server.mjs");

const checks = [];
const check = (name, ok, detail = "") => checks.push({ name, ok, detail });

const agent = new OvtAgent(new LocalTestOwnerSigner(), { serverCmd: process.execPath, serverArgs: [serverPath] });
await agent.connect();

try {
  // 1. The autonomous happy-path loop.
  const out = await agent.runOnce();
  check("loop reaches FINALIZED autonomously", out.terminalStage === "FINALIZED", out.terminalStage ?? `${out.reasonCode}`);
  check("operator_sig + owner_sig both verified at ingress (derived, not injected)", out.operatorSigPresent && out.ownerSigPresent);
  check("trace was generated by the MCP executor (no manual step assertions)", out.traceWasGenerated === true);
  check("event sequence is the full lifecycle", out.events.join("→") === "cal.created→cal.signed→cal.validated→cal.executed→cal.settled→cal.finalized", out.events.join("→"));
  check("finalized state root + event-log root produced", !!out.stateRootAfter && !!out.eventLogRoot);

  // 2. Replay safety / nonce lifecycle — same CAL twice over one state.
  const rep = await agent.runNonceReplay();
  check("replay: first submission FINALIZED", rep.first === "FINALIZED", rep.first ?? "");
  check("replay: second (same nonce) NOT finalized", rep.second !== "FINALIZED", `${rep.second}/${rep.secondReason}`);

  // 3. Negative control — corrupt operator_sig → loop must NOT finalize (real sig plumbing).
  const neg = await agent.runOnce({ tamperOperatorSig: true });
  check("negative control: tampered operator_sig → operatorSigPresent=false", neg.operatorSigPresent === false);
  check("negative control: tampered operator_sig → NOT finalized", neg.terminalStage !== "FINALIZED", `${neg.terminalStage}/${neg.reasonCode}`);
} finally {
  await agent.close();
}

console.log(`\nOVT-1 H1.4 — autonomous agent loop\n`);
let allOk = true;
for (const c of checks) { console.log(`  ${c.ok ? "✅" : "❌"} ${c.name}${c.detail ? "  — " + c.detail : ""}`); allOk = allOk && c.ok; }
console.log(`\n${allOk ? "✅ OVT-1 H1.4: agent completes the full cycle autonomously (construct→sign→execute→submit→FINALIZED), with replay safety + real sig plumbing." : "❌ OVT-1 H1.4 FAILED"}`);
process.exit(allOk ? 0 : 1);
